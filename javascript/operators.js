//Assignment operator
a = 10
console.log(a)

//Arithmetic operator
b = 20
console.log(a+b)
console.log(a-b)
console.log(a*b)
console.log(a/b)
console.log(a%b)

//Unary Operator
a++
b--
console.log(a)
console.log(b)

//Relational operator
console.log(a<b)
console.log(a>b)
console.log(a<=b)
console.log(a>=b)
console.log(a==b)
console.log(a===b)
console.log(a!=b)
console.log(a!==b)

//Logical operator
if(a == 11 && b == 19){
    console.log("true")
}
else{
    console.log("false")
}
if(a == 9 || b == 19){
    console.log("true")
}
else{
    console.log("false")
}
console.log(!a)

//bitwise operator
console.log(a&b)
console.log(a|b)
console.log(a^b)
console.log(a>>2)
console.log(a<<2)

